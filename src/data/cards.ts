import type { TarotCard } from '../types/tarot';

// Helper to create a card
function card(
  id: number, name: string, nameZh: string, arcana: 'major' | 'minor',
  suit: 'wands' | 'cups' | 'swords' | 'pentacles' | null,
  number: number, rank: any, element: 'fire' | 'water' | 'air' | 'earth' | 'spirit',
  symbol: string, positive: boolean, descZh: string,
  kwUpright: string[], kwReversed: string[],
  up: Partial<Record<string,string>>, rev: Partial<Record<string,string>>
): TarotCard {
  const areas = ['general','love','study','work','travel','social'];
  const mUp: any = {}, mRev: any = {};
  for (const a of areas) {
    mUp[a] = up[a] || up.general || '';
    mRev[a] = rev[a] || rev.general || '';
  }
  return {
    id, name, nameZh, arcana, suit, number, rank, element, symbol,
    isGenerallyPositive: positive, descriptionZh: descZh,
    keywordsUpright: kwUpright, keywordsReversed: kwReversed,
    meaningsUpright: mUp, meaningsReversed: mRev,
  };
}

